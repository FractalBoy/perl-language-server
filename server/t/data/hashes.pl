my %hash = (a => 1, b => 2); # (0, 3) -> (0, 3)
%hash = (a => 1); # (1, 0) -> (0, 3)

sub hash1 {
    my %hash = (a => 1, b => 2, c => 3); # (4, 7) -> (4, 7)

    %hash = (d => 4); # (6, 4) -> (4, 7)
    $hash{a} = 2; # (7, 4) -> (4, 7)
    $hash{'a'} = 3; # (8, 4) -> (4, 7)
}

sub hash2 {
    %hash = (d => 4); # (12, 4) -> (0, 3)
    $hash{b} = 4; # (13, 4) -> (0, 3)
    $hash{'c'} = 5; # (14, 4) -> (0, 3)
}
